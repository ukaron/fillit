#include "fillit.h"

void	ft_print_cube(char *cube[5])
{
	int j;
	int c;

	j = 0;
	c = 65;

	while (cube[j] != '\0')
	{
		ft_putstr(cube[j]);
		ft_putchar('\n');
		j++;
	}
}
