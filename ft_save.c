#include "fillit.h"

void	ft_save(char **cube, int c)
{
	int 	i;
	char	*res[50][5];

	i = 0;
	while (cube[i] != '\0')
	{
		res[c][i] = ft_strnew(ft_strlen(cube[i]) + 1);
		ft_strcpy(res[c][i], cube[i]);
		i++;
	}
}
