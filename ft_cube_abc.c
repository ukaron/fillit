#include "fillit.h"

void	ft_cube_abc(char **cube, int c)
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (cube[i][j] != '\0')
		{
			if (cube[i][j] == '#')
				cube[i][j] = cube[i][j] + c + 30;
			j++;
		}
		i++;
	}
}
